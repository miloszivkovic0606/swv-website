  <script type="text/javascript">
    $(document).ready(function () {
      $('#contact_submit').click(function (e) {
        e.preventDefault();
        e.stopPropagation();

        var form = $('#form_contact');
        var validobj = form.validate({
          errorPlacement: function (error, element) {
            $(element).parent().append(error);
          },
          errorClass: 'has-error',
          rules: {
            email: {
              required: true,
              email: true
            },
            subject: {
              required: true
            },
            phone: {
              required: true
            },
            firma: {
              required: true
            },
            first_name: {
              required: true
            },
            last_name: {
              required: true
            }


          },
          highlight: function (element, errorClass, validClass) {
            var elem = $(element);
            $(elem).addClass(errorClass);
          },
          unhighlight: function (element, errorClass, validClass) {
            var elem = $(element);
            $(elem).removeClass(errorClass);
          }
        });

        if (form.valid()) {
          $('#contact_submit').addClass('disabled');

          var data = {};
          data.email = $('#email').val();
          data.subject = $('#subject').val();
          data.message = $('#contactMessage').val();
          data.phone = $('#phone').val();
          data.firma = $('#firma').val();
          data.first_name = $('#first_name').val();
          data.last_name = $('#last_name').val();

          $.ajax({
            type: 'post',
            data: $.param({
              data: data
            }),
            dataType: 'json',
            url: "proccess-contact.php",
            success: function (data) {
              $('#message').html(
                'Ihre Anfrage wurde erfolgreich versendet! Ein Vertriebsmitarbeiter wird Sie binnen 24 Stunden kontaktieren'
                );
              $('#message').css("border", "2px solid #C2D1D9");
              $('#form_contact')[0].reset();
            },
            complete: function () {},
            error: function (jqXHR, textStatus, errorThrown) {
              alert(textStatus + '-' + errorThrown);
            }
          });
        }
      });
    });
